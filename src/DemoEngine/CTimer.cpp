#include "CTimer.h"
////////////////////////////////////////////////////////////////////////////////
void
DemoEngine::CTimer::Update()
{
  m_CurrentTime = SDL_GetTicks(); //store current time
}
////////////////////////////////////////////////////////////////////////////////
DemoEngine::CTimer::CTimer() 
{
  Reset();
}
////////////////////////////////////////////////////////////////////////////////
void 
DemoEngine::CTimer::Reset()
{
  m_PreviousTime = m_CurrentTime = SDL_GetTicks();
}
////////////////////////////////////////////////////////////////////////////////
float 
DemoEngine::CTimer::GetPassedTime()
{
  return (GetPassedTimeMS() * 0.001f);
}
////////////////////////////////////////////////////////////////////////////////
Uint32 
DemoEngine::CTimer::GetPassedTimeMS()
{
  return (m_CurrentTime - m_PreviousTime);
}
